#!/usr/bin/env node
const fs = require('fs/promises');
const path = require('path');
const readline = require('readline');
const { regenerateSitemap } = require('../server/api/sitemap.js');

const DIRS = [
  'rules',
  'changelog',
  'changelog/raw',
  'memory',
  'wiki',
  'wiki/raw',
  'wiki/pages',
  'prototype',
  'prototype/resources',
  'prototype/resources/js',
  'prototype/resources/css',
  'prototype/components',
  'prototype/pages'
];

async function exists(p) {
  try { await fs.access(p); return true; } catch { return false; }
}

async function init(projectRoot) {
  for (const dir of DIRS) {
    await fs.mkdir(path.join(projectRoot, dir), { recursive: true });
  }

  await regenerateSitemap(projectRoot);

  const templateRoot = path.resolve(__dirname, '../templates');
  const projectTplRoot = path.join(templateRoot, 'project');

  const agentsPath = path.join(projectRoot, 'AGENTS.md');
  if (!await exists(agentsPath)) {
    const agentsTpl = path.join(projectTplRoot, 'AGENTS.md');
    if (await exists(agentsTpl)) {
      await fs.copyFile(agentsTpl, agentsPath);
    } else {
      await fs.writeFile(
        agentsPath,
        `# Project Agents Rules\n\n本文件为 Agent 全局规则。\n`,
        'utf-8'
      );
    }
  }

  const claudePath = path.join(projectRoot, 'CLAUDE.md');
  if (!await exists(claudePath)) {
    const claudeTpl = path.join(projectTplRoot, 'CLAUDE.md');
    if (await exists(claudeTpl)) {
      await fs.copyFile(claudeTpl, claudePath);
    }
  }

  const readmePath = path.join(projectRoot, 'readme.md');
  if (!await exists(readmePath)) {
    await fs.writeFile(
      readmePath,
      `# Axhost-Make Project\n\nGenerated by axhost-make init.\n`,
      'utf-8'
    );
  }

  // Copy rules/design.md template
  const designMdPath = path.join(projectRoot, 'rules', 'design.md');
  if (!await exists(designMdPath)) {
    const designTpl = path.join(projectTplRoot, 'rules', 'design.md');
    if (await exists(designTpl)) {
      await fs.copyFile(designTpl, designMdPath);
    }
  }

  // Copy RESOURCES.md template
  const resourcesMdPath = path.join(projectRoot, 'RESOURCES.md');
  if (!await exists(resourcesMdPath)) {
    const resourcesTpl = path.join(projectTplRoot, 'RESOURCES.md');
    if (await exists(resourcesTpl)) {
      await fs.copyFile(resourcesTpl, resourcesMdPath);
    }
  }

  // Copy COMPONENTS.md template
  const componentsMdPath = path.join(projectRoot, 'COMPONENTS.md');
  if (!await exists(componentsMdPath)) {
    const componentsTpl = path.join(projectTplRoot, 'COMPONENTS.md');
    if (await exists(componentsTpl)) {
      await fs.copyFile(componentsTpl, componentsMdPath);
    }
  }

  // 框架资源由 /client/ 统一提供，不再复制到项目

  console.log('Axhost-Make project initialized successfully.');
  console.log('Generated files:');
  console.log('  - prototype/sitemap.js');
  console.log('  - AGENTS.md (if not exists)');
  console.log('  - readme.md (if not exists)');
}

async function syncStartScripts(workspaceRoot) {
  const templateRoot = path.resolve(__dirname, '../templates');
  const scriptDir = path.join(templateRoot, 'start-script');
  const files = ['start.ps1', 'start.cmd', 'start.sh'];
  let copied = 0;
  for (const file of files) {
    const src = path.join(scriptDir, file);
    const dest = path.join(workspaceRoot, file);
    if (await exists(src)) {
      await fs.copyFile(src, dest);
      copied++;
    }
  }
  if (copied > 0) {
    console.log(`  - Synced ${copied} start script(s)`);
  }
}

async function initWorkspace(currentDir, options = {}) {
  const { nonInteractive } = options;
  const currentDirName = path.basename(currentDir);
  let workspaceRoot;

  if (currentDirName === 'axhost-make') {
    workspaceRoot = path.dirname(currentDir);
  } else if (await exists(path.join(currentDir, 'axhost-make'))) {
    workspaceRoot = currentDir;
  } else {
    console.error('请在 axhost-make 目录内或其父级工作空间目录中运行 init 命令。');
    process.exit(1);
  }

  // 同步启动脚本（幂等）
  await syncStartScripts(workspaceRoot);

  const projectsDir = path.join(workspaceRoot, 'projects');
  if (await exists(projectsDir)) {
    console.log('工作空间已初始化，projects 目录已存在。');
    return;
  }

  // 检查工作空间根目录下是否仅有 axhost-make 一个目录
  const items = await fs.readdir(workspaceRoot, { withFileTypes: true });
  const dirs = items.filter(d => d.isDirectory()).map(d => d.name);

  if (dirs.length > 1 || (dirs.length === 1 && dirs[0] !== 'axhost-make')) {
    console.error('工作空间目录下已有其他目录，初始化失败。');
    console.error('请确保工作空间下仅有 axhost-make 目录。');
    process.exit(1);
  }

  // 1. 创建空的 projects 目录和 .projects.json
  await fs.mkdir(projectsDir, { recursive: true });
  await fs.writeFile(
    path.join(projectsDir, '.projects.json'),
    JSON.stringify({ projects: [] }, null, 2) + '\n',
    'utf-8'
  );

  // 2. 在父级目录生成 package.json
  const pkgPath = path.join(workspaceRoot, 'package.json');
  if (!await exists(pkgPath)) {
    const pkg = {
      name: 'axhost-workspace',
      version: '1.0.0',
      description: 'Axhost-Make workspace',
      scripts: {
        serve: 'node axhost-make/bin/axhost-make.js serve',
        build: 'node axhost-make/bin/axhost-make.js build',
        update: 'node axhost-make/bin/axhost-make.js update --all',
        preview: 'node axhost-make/bin/axhost-make.js preview'
      }
    };
    await fs.writeFile(pkgPath, JSON.stringify(pkg, null, 2) + '\n', 'utf-8');
    console.log('  - Created package.json');
  }

  console.log('\nAxhost-Make workspace initialized successfully.');
  console.log('Generated files:');
  console.log('  - projects/');
  console.log('  - projects/.projects.json');
  console.log('  - package.json');

  // 3. 提示是否需要立即启动服务
  if (nonInteractive) {
    console.log('你可以稍后通过 npm run serve 启动服务。');
  } else {
    const rl = readline.createInterface({
      input: process.stdin,
      output: process.stdout
    });

    const answer = await new Promise(resolve => {
      rl.question('\n是否立即启动服务？(y/n): ', resolve);
    });
    rl.close();

    const shouldStart = answer.trim().toLowerCase();
    if (shouldStart === 'y' || shouldStart === 'yes' || shouldStart === '') {
      console.log('正在启动服务...');
      const { startServer } = require('../server/index.js');
      startServer({ port: 3820, host: '127.0.0.1', projectRoot: workspaceRoot });
    } else {
      console.log('你可以稍后通过 npm run serve 启动服务。');
    }
  }
}

module.exports = { init, initWorkspace, syncStartScripts };

if (require.main === module) {
  const currentDir = process.cwd();
  initWorkspace(currentDir).catch(err => {
    console.error('Init failed:', err);
    process.exit(1);
  });
}
